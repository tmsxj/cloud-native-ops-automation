#!/bin/bash

###############################################################################
# 脚本名称：network-troubleshoot.sh
# 功能说明：网络故障排查脚本
# 适用场景：网络连接问题、无法访问服务、延迟高
# 使用方法：sudo ./network-troubleshoot.sh
# 输出说明：显示网络状态和排查结果，异常项用红色标注
###############################################################################

# 颜色定义
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

echo -e "${BLUE}=========================================="
echo -e "  网络故障排查脚本"
echo -e "==========================================${NC}"
echo ""

#-------------------------------------------------------------------------------
# 1. 检查网络接口状态
#-------------------------------------------------------------------------------
echo -e "${YELLOW}[1] 网络接口状态 ${NC}"
echo "----------------------------------------------"
echo "网络接口信息:"
ip addr show | grep -E "inet|inet6|link/ether" | head -20
echo ""

echo "接口状态:"
ip link show | grep -E "^[0-9]|UP|DOWN"
echo ""

#-------------------------------------------------------------------------------
# 2. 检查路由表
#-------------------------------------------------------------------------------
echo -e "${YELLOW}[2] 路由表 ${NC}"
echo "----------------------------------------------"
echo "路由信息:"
ip route show
echo ""

echo "默认网关: $(ip route | grep default | awk '{print $3}')"
echo ""

#-------------------------------------------------------------------------------
# 3. 检查 DNS 配置
#-------------------------------------------------------------------------------
echo -e "${YELLOW}[3] DNS 配置 ${NC}"
echo "----------------------------------------------"
echo "/etc/resolv.conf 内容:"
cat /etc/resolv.conf 2>/dev/null
echo ""

echo "测试 DNS 解析:"
if nslookup google.com 2>/dev/null | grep -q "Address"; then
    echo -e "${GREEN}✓ DNS 解析正常${NC}"
else
    echo -e "${RED}✗ DNS 解析失败${NC}"
fi
echo ""

#-------------------------------------------------------------------------------
# 4. 检查网络连接状态
#-------------------------------------------------------------------------------
echo -e "${YELLOW}[4] 网络连接状态 ${NC}"
echo "----------------------------------------------"
echo "连接状态统计:"
ss -s
echo ""

echo "监听端口:"
ss -ltn | head -10
echo ""

#-------------------------------------------------------------------------------
# 5. 测试网络连通性
#-------------------------------------------------------------------------------
echo -e "${YELLOW}[5] 网络连通性测试 ${NC}"
echo "----------------------------------------------"

# 测试网关连通性
GATEWAY=$(ip route | grep default | awk '{print $3}')
echo "测试网关 $GATEWAY:"
if ping -c 3 $GATEWAY &>/dev/null; then
    echo -e "${GREEN}✓ 网关可达${NC}"
else
    echo -e "${RED}✗ 网关不可达${NC}"
fi
echo ""

# 测试外网连通性
echo "测试外网连通性:"
if ping -c 3 8.8.8.8 &>/dev/null; then
    echo -e "${GREEN}✓ 外网可达${NC}"
else
    echo -e "${RED}✗ 外网不可达${NC}"
fi
echo ""

# 测试 DNS 解析
echo "测试域名解析:"
if ping -c 3 google.com &>/dev/null; then
    echo -e "${GREEN}✓ DNS 解析正常${NC}"
else
    echo -e "${RED}✗ DNS 解析失败${NC}"
fi
echo ""

#-------------------------------------------------------------------------------
# 6. 检查防火墙规则
#-------------------------------------------------------------------------------
echo -e "${YELLOW}[6] 防火墙规则 ${NC}"
echo "----------------------------------------------"

if command -v ufw &> /dev/null; then
    echo "ufw 状态:"
    ufw status
elif command -v firewall-cmd &> /dev/null; then
    echo "firewalld 状态:"
    firewall-cmd --list-all
else
    echo "查看 iptables 规则:"
    iptables -L -n | head -20
fi
echo ""

#-------------------------------------------------------------------------------
# 7. 检查网络服务状态
#-------------------------------------------------------------------------------
echo -e "${YELLOW}[7] 网络服务状态 ${NC}"
echo "----------------------------------------------"

NETWORK_SERVICES=("network" "networkd" "NetworkManager" "sshd")

for service in "${NETWORK_SERVICES[@]}"; do
    if systemctl is-active --quiet "$service"; then
        echo -e "  ${GREEN}✓ $service${NC}"
    else
        echo -e "  ${RED}✗ $service${NC}"
    fi
done
echo ""

#-------------------------------------------------------------------------------
# 8. 检查网络性能
#-------------------------------------------------------------------------------
echo -e "${YELLOW}[8] 网络性能测试 ${NC}"
echo "----------------------------------------------"

echo "测试到网关的延迟:"
ping -c 5 $GATEWAY | tail -1
echo ""

echo "测试带宽（使用 speedtest-cli，如安装）:"
if command -v speedtest-cli &> /dev/null; then
    speedtest-cli --simple
else
    echo "speedtest-cli 未安装，跳过带宽测试"
    echo "安装: pip install speedtest-cli"
fi
echo ""

#-------------------------------------------------------------------------------
# 9. 检查网络错误日志
#-------------------------------------------------------------------------------
echo -e "${YELLOW}[9] 网络错误日志 ${NC}"
echo "----------------------------------------------"

echo "最近的网络相关日志:"
journalctl -p err -u network 2>/dev/null | tail -10 || echo "无网络错误日志"
echo ""

#-------------------------------------------------------------------------------
# 排查结论
#-------------------------------------------------------------------------------
echo -e "${BLUE}=========================================="
echo -e "  排查结论"
echo -e "==========================================${NC}"
echo ""

echo "常见网络问题及解决方案:"
echo ""
echo "[问题1] 无法访问外网"
echo "  可能原因: 网关配置错误、DNS 配置错误、防火墙阻止"
echo "  解决方案:"
echo "    检查网关: ip route show | grep default"
echo "    检查 DNS: cat /etc/resolv.conf"
echo "    测试连通性: ping 8.8.8.8"
echo ""

echo "[问题2] 特定端口无法访问"
echo "  可能原因: 服务未启动、防火墙阻止、端口未监听"
echo "  解决方案:"
echo "    检查监听: ss -ltn | grep <端口>"
echo "    检查防火墙: ufw status 或 firewall-cmd --list-all"
echo ""

echo "[问题3] 网络延迟高"
echo "  可能原因: 网络拥塞、路由问题、DNS 延迟"
echo "  解决方案:"
echo "    检查路由: traceroute 8.8.8.8"
echo "    检查 DNS: nslookup google.com"
echo ""

echo "[问题4] 丢包严重"
echo "  可能原因: 网络质量差、MTU 不匹配、交换机问题"
echo "  解决方案:"
echo "    测试丢包: ping -c 100 <目标>"
echo "    检查 MTU: ip link show | grep mtu"
echo ""

echo -e "${BLUE}=========================================="
echo -e "  网络排查完成"
echo -e "==========================================${NC}"