#!/bin/bash

###############################################################################
# 脚本名称：join-nodes.sh
# 功能说明：节点加入脚本（在master2/master3/worker节点上执行）
# 适用场景：将节点加入已初始化的Kubernetes集群
# 使用方法：sudo ./join-nodes.sh <token> <hash> [--control-plane]
###############################################################################

set -e

# 颜色定义
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

echo -e "${BLUE}=========================================="
echo -e "  节点加入Kubernetes集群"
echo -e "==========================================${NC}"
echo ""

#-------------------------------------------------------------------------------
# 解析参数
#-------------------------------------------------------------------------------
echo -e "${YELLOW}[1] 解析参数 ${NC}"
echo "----------------------------------------------"

TOKEN="$1"
HASH="$2"
CONTROL_PLANE=""
CERT_KEY=""

if [ "$3" = "--control-plane" ]; then
    CONTROL_PLANE="--control-plane"
    CERT_KEY="$4"
    echo "加入模式: 控制平面节点"
else
    echo "加入模式: Worker节点"
fi

echo "Token: $TOKEN"
echo "Hash: $HASH"
echo ""

#-------------------------------------------------------------------------------
# 清理旧配置
#-------------------------------------------------------------------------------
echo -e "${YELLOW}[2] 清理旧配置 ${NC}"
echo "----------------------------------------------"

kubeadm reset -f || true
rm -rf /etc/kubernetes /var/lib/etcd /var/lib/kubelet ~/.kube
iptables -F && iptables -t nat -F && iptables -t mangle -F

echo -e "${GREEN}✓ 旧配置已清理${NC}"
echo ""

#-------------------------------------------------------------------------------
# 加入集群
#-------------------------------------------------------------------------------
echo -e "${YELLOW}[3] 加入集群 ${NC}"
echo "----------------------------------------------"

if [ -n "$CONTROL_PLANE" ] && [ -n "$CERT_KEY" ]; then
    kubeadm join 192.168.1.51:6443 \
        --token "$TOKEN" \
        --discovery-token-ca-cert-hash "sha256:$HASH" \
        --control-plane \
        --certificate-key "$CERT_KEY"
else
    kubeadm join 192.168.1.51:6443 \
        --token "$TOKEN" \
        --discovery-token-ca-cert-hash "sha256:$HASH"
fi

echo -e "${GREEN}✓ 节点加入成功${NC}"
echo ""

#-------------------------------------------------------------------------------
# 完成总结
#-------------------------------------------------------------------------------
echo -e "${BLUE}=========================================="
echo -e "  节点加入完成"
echo -e "==========================================${NC}"
echo ""

echo "在master1上执行以下命令验证:"
echo "  kubectl get nodes"
echo ""

echo -e "${BLUE}==========================================${NC}"